public class Product {
    private String id;
    private String name;
    private Float pricr;
    private  String ms;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getPricr() {
        return pricr;
    }

    public void setPricr(Float pricr) {
        this.pricr = pricr;
    }

    public String getMs() {
        return ms;
    }

    public void setMs(String ms) {
        this.ms = ms;
    }
}
